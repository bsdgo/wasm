float sum(float a,float b){
  return a + b;
}

int increment(int a){
  return a+1;
}

float minus(float a,float b){
  return a - b;
}

int max(unsigned int a,unsigned int b){
  if (a > b){
    return a;
  }
  return b;
}

float divide(unsigned int a,unsigned int b){
  return float(a)/float(b);
}

unsigned int divide2(unsigned int a,unsigned int b){
  return a/b;
}